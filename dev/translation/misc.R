## RANDOM MISSING TRANSLATION ##################################################

translation_misc <- 
  tibble(en = "",
         fr = "") |> 
  add_row(en = " ",
          fr = " ") |> 
  add_row(en = "Number",
          fr = "Nombre") |> 
  add_row(en = "Percentage",
          fr = "Pourcentage") |> 
  add_row(en = "Indicator",
          fr = "Indicateur")